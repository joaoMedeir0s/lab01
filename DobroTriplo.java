import java.util.Scanner;

public class DobroTriplo{
	public static void main(String[] args){
		Scanner input = new Scanner(System.in);
		int num = input.nextInt();
		int dobro = num * 2;
		int triplo = num * 3;
		System.out.println("dobro: " + dobro + ", triplo: " + triplo);
	}
}
		

